"""
Suggestion model - stores improvement suggestions generated by prompt tuning jobs.
"""

import uuid
from sqlalchemy import Column, String, DateTime, ForeignKey, Integer
from sqlalchemy.sql import func
from sqlalchemy.dialects.postgresql import UUID, JSONB

from overmind_core.db.base import Base


class Suggestion(Base):
    __tablename__ = "suggestions"

    suggestion_id = Column(
        UUID(as_uuid=True),
        primary_key=True,
        default=uuid.uuid4,
    )
    prompt_slug = Column(String, nullable=False, index=True)
    project_id = Column(
        UUID(as_uuid=True),
        ForeignKey("projects.project_id"),
        nullable=False,
        index=True,
    )
    job_id = Column(
        UUID(as_uuid=True), ForeignKey("jobs.job_id"), nullable=True, index=True
    )

    title = Column(String, nullable=False)
    description = Column(String, nullable=False)

    # The improved prompt text (if this suggestion is about a new prompt version)
    new_prompt_text = Column(String, nullable=True)
    new_prompt_version = Column(Integer, nullable=True)

    # Scores generated by the improved prompt
    # Format: {"avg_correctness": 0.85, "scored_spans": 50, "latency_ms": 1234, ...}
    scores = Column(JSONB, nullable=True)

    # pending | accepted | dismissed
    status = Column(String, nullable=False, default="pending")
    vote = Column(Integer, nullable=False, default=0)  # upvote = 1 | downvote = -1
    feedback = Column(String, nullable=True)  # text feedback

    created_at = Column(DateTime(timezone=True), server_default=func.now())

    def is_good_suggestion(self) -> bool:
        return self.vote == 1 or self.feedback == "accepted"
